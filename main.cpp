#define CATCH_CONFIG_MAIN
#include <iostream>
#include <vector>
#include <queue>
#include "catch.hpp"
class Data{
public:
    Data(int ai, int aj):i(ai), j(aj){}
    int i=0;
    int j=0;
};
bool findPath(std::vector<std::vector<int>> &aInputMaze){
    if(aInputMaze[0].size()!=aInputMaze.size()){
        std::cout<<"Maze is not correct!"<<std::endl;
        return false;
    }
    if(aInputMaze[0][0]==0 || aInputMaze[aInputMaze.size()-1][aInputMaze.size()-1]==0){
        return false;
    }
    std::queue<Data> q;
    Data temp(0,0);
    q.push(temp);
    while(!q.empty()) {
        temp=q.front();
        q.pop();
        if (temp.i == aInputMaze.size() - 1 && temp.j == aInputMaze.size() - 1) {
            return true;
        }
        if (temp.i + 1 < aInputMaze.size() && aInputMaze[temp.i + 1][temp.j] == 1) {
            temp.i += 1;
            q.push(temp);
            temp.i-=1;
            aInputMaze[temp.i + 1][temp.j] = 0;
        }
        if (temp.j + 1 < aInputMaze.size() && aInputMaze[temp.i][temp.j + 1] == 1) {
            temp.j += 1;
            q.push(temp);
            temp.j-=1;
            aInputMaze[temp.i][temp.j + 1] = 0;
        }
        if (temp.j - 1 >= 0 && aInputMaze[temp.i][temp.j - 1] == 1) {
            temp.j -= 1;
            q.push(temp);
            temp.j+=1;
            aInputMaze[temp.i][temp.j - 1] = 0;
        }
        if (temp.i - 1 >= 0 && aInputMaze[temp.i - 1][temp.j] == 1) {
            temp.i-= 1;
            q.push(temp);
            temp.i+=1;
            aInputMaze[temp.i - 1][temp.j] = 0;
        }
    }
    return false;
}
int run() {
    int mazeDimension;
    int inputUnit;
    bool flag;
    std::cout << "Print dimension of matrix" << std::endl;
    std::cin>>mazeDimension;
    std::vector<std::vector<int>> inputMaze(mazeDimension, std::vector<int>(mazeDimension, 0));
    while (true) {
        flag= false;
        for (int i = 0; i < mazeDimension; i++) {
            for (int j = 0; j < mazeDimension; j++) {
                std::cin >> inputUnit;
                if (inputUnit != 1 && inputUnit != 0) {
                    std::cout << "Print maze correctly!\n";
                    flag=true;
                    break;
                }
                inputMaze[i][j] = inputUnit;
            }
            if(flag){break;}
        }
        if(flag){continue;}
        std::cout<<findPath(inputMaze);
        return  0;
    }
}
